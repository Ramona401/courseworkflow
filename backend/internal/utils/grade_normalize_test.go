package utils

// grade_normalize_test.go — 年级转换工具单元测试
//
// v89-5新增：覆盖 grade_normalize.go 中 NormalizeGradeToNumber 函数
//
// 测试范围：
//   - 阿拉伯数字直接提取
//   - 范围格式保留
//   - 小学段别名转换
//   - 初高中别名转换
//   - 中文数字转换（含十一、十二特殊处理）
//   - 边界和异常输入

import "testing"

func TestNormalizeGradeToNumber(t *testing.T) {
	tests := []struct {
		name     string
		input    string
		expected string
	}{
		// 已含阿拉伯数字
		{"纯数字", "7", "7"},
		{"数字+年级", "7年级", "7"},
		{"数字+其他文字", "第5年级", "5"},
		{"两位数字", "12年级", "12"},

		// 范围格式（含横杠+数字）直接保留
		{"范围3-6", "3-6", "3-6"},
		{"范围1-9", "1-9", "1-9"},
		{"范围7-9", "7-9", "7-9"},

		// 小学段别名
		{"小学低段", "小学低段", "1-2"},
		{"小学中段", "小学中段", "3-4"},
		{"小学高段", "小学高段", "5-6"},

		// 初高中别名
		{"初一", "初一", "7"},
		{"初二", "初二", "8"},
		{"初三", "初三", "9"},
		{"高一", "高一", "10"},
		{"高二", "高二", "11"},
		{"高三", "高三", "12"},

		// 中文数字
		{"一年级", "一年级", "1"},
		{"二年级", "二年级", "2"},
		{"三年级", "三年级", "3"},
		{"七年级", "七年级", "7"},
		{"九年级", "九年级", "9"},
		{"十年级", "十年级", "10"},
		{"十一年级", "十一年级", "11"},
		{"十二年级", "十二年级", "12"},

		// 边界
		{"空字符串", "", ""},
		{"纯空格", "  ", "  "},
		{"无法识别", "幼儿园", "幼儿园"},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := NormalizeGradeToNumber(tt.input)
			if got != tt.expected {
				t.Errorf("NormalizeGradeToNumber(%q) = %q, want %q", tt.input, got, tt.expected)
			}
		})
	}
}
